function cm = cpc_viridis(n)
% CPC_VIRIDIS  Viridis colormap (perceptually uniform, matplotlib default).
% Usage: colormap(cpc_viridis(256))
if nargin < 1; n = 256; end
stops = [ ...
    0.267004 0.004874 0.329415; ...
    0.282656 0.100196 0.422160; ...
    0.253935 0.265254 0.529983; ...
    0.206756 0.371758 0.553117; ...
    0.163625 0.471133 0.558148; ...
    0.127568 0.566949 0.550556; ...
    0.134692 0.658636 0.517649; ...
    0.266941 0.748751 0.440573; ...
    0.477504 0.821444 0.318195; ...
    0.741388 0.873449 0.149561; ...
    0.993248 0.906157 0.143936];
xs = linspace(0, 1, size(stops,1));
xq = linspace(0, 1, n);
cm = [interp1(xs, stops(:,1), xq, 'linear')', ...
      interp1(xs, stops(:,2), xq, 'linear')', ...
      interp1(xs, stops(:,3), xq, 'linear')'];
end
